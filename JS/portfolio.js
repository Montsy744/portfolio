const renderHtmlStack = (imgStack) => {
  let htmlTemp = "";

  imgStack.forEach((prop) => {
    htmlTemp += `<img src="${prop}" alt="logo de la Stack"  class="stack"></img>`;
  });

  return htmlTemp;
};

const renderHtmlPortfolio = ({
  imgProjet,
  title,
  description,
  lienCode,
  lienSite,
  imgStack,
}) => {
  let liensProjet = "";

  if (lienSite === ``) {
    liensProjet += `
        <a href="${lienCode}" target="_blank" class="btnCard code">Voir le code</a>
    `;
  } else {
    liensProjet += `
        <a href="${lienSite}" target="_blank" class="btnCard site">Voir le projet</a>
        <a href="${lienCode}" target="_blank" class="btnCard code">Voir le code</a>
    `;
  }

  return `<div class="card">
            <div class="imgContainer">
                <img src="${imgProjet}" alt="Image du lancement du site" class="card__image">
                <div>
                    ${renderHtmlStack(imgStack)}
                </div>
            </div>
            <div class="card__content">
                <p class="card__title">${title}</p>
                <p class="card__description">${description}</p>
                <div class="btnContainer">
                    ${liensProjet}
                </div>
            </div>
        </div>`;
};

